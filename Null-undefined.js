// Null and Undefined

// both represent "no value"

// undefined => "Hey!! JavaScipt can not find value for this thing"

// null => "Developer sets the value"

/*
     "undefined"

    - variable without value
    - missing function arguments
    - missing object properties
*/

let numberNull = 20 + null; // 20 + 0; => NULL is something that we set
console.log(numberNull); // 20

let numberUndfined = 20 + undefined; //
console.log(numberUndfined); // NaN  => set by JavaScript, it can't find values
